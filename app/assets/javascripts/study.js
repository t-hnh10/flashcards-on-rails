function showDefinition() {
    var x = document.getElementById("showDef");
    if (x.style.display === "none") {
        x.style.display = "inline";
    } else {
        x.style.display = "none";
    }
}

function showTransliteration() {
    var x = document.getElementById("showTrans");
    if (x.style.display === "none") {
        x.style.display = "inline";
    } else {
        x.style.display = "none";
    }
}